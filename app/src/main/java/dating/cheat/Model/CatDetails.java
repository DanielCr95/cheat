package dating.cheat.Model;

public class CatDetails {
    private String idOpener;
    private String refCategory;
    private String message;
    private String explanation;
    private String lockStatus;

    public String getIdOpener() {
        return idOpener;
    }

    public void setIdOpener(String idOpener) {
        this.idOpener = idOpener;
    }

    public String getRefCategory() {
        return refCategory;
    }

    public void setRefCategory(String refCategory) {
        this.refCategory = refCategory;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getExplanation() {
        return explanation;
    }

    public void setExplanation(String explanation) {
        this.explanation = explanation;
    }

    public String getLockStatus() {
        return lockStatus;
    }

    public void setLockStatus(String lockStatus) {
        this.lockStatus = lockStatus;
    }
}
